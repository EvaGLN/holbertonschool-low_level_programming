#include "3-calc.h"

/**
* op_add - addition
* @a: 1st num
* @b: 2nd num
* Return: sum result
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtraction
* @a: 1st num
* @b: 2nd num
* Return: sub result
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplication
* @a: 1st num
* @b: 2nd num
* Return: mul result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division
* @a: 1st num
* @b: 2nd num
* Return: div result
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - modulo
* @a: 1st num
* @b: 2nd num
* Return: remain of modulo
*/
int op_mod(int a, int b)
{
	return (a % b);
}
